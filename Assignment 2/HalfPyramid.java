class HalfPyramid
{
	public static void main(String args[])
	{
	for(int i=1;i<=6;i++) //for loop for printing *
		{
			 for(int j=1;j<=i;j++)
			 {
				 System.out.print("*"+" ");
			 }
			 System.out.println();
		}
	
	}
}

