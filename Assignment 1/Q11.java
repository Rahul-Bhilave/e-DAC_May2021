class Q11
{
public static final double pi=3.14159265;

	public static void main(String args[])
	{
		double peri,area,rad=7.5;
		peri=2*pi*rad;
		area=pi*rad*rad;
		System.out.println("Perimeter"+peri);
		System.out.println("Area"+area);
	}
}

